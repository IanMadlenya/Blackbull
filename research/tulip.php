<h1>TULIP</h1>
<h4>Blackbull Company Analysis</h4>
<h6>28<sup>th</sup>&nbsp;November 2010</h6>
<p><span class="dropcap">T</span>ULIP is a data telecom service and IT solutions provider. TULIP is india's largest VPN service provider and it has been the front runner in providing &amp; managing wide area networks for various verticals. TULIP offers following products and services:</p>
<ol>
<li>Wide area network integration</li>
<li>Corporate data connectivity</li>
<li>Internet services</li>
<li>IT consulting</li>
<li>E-Governance</li>
</ol>
<h3>Telecommunications domain overview</h3>
<p>The telecommunication domain includes mobile network, internet network, wireless internet, telephony services. Currently only 25% of India has telecom connectivity. The Indian telecommunication industry is one of the fastest growing industries in the world.</p>
<p>One thing is safe to assume. The mobile devices becoming a part of our lives, and internet becoming an addiction, there will be tremendous growth potential in wireless network solutions and wireless broadband solutions.</p>
<h3>TULIP advantage</h3>
<p>Although telecommunications domain includes variety of services, tulip mainly operates in the VPN connectivity. 43% of tulip&#8217;s business comes from the VPN connectivity.</p>
<p>Lets discuss the scope of VPN connectivity in india. The major business needs for VPN are as follows:</p>
<ul>
<li><h4>Core banking solutions</h4><p>Banks have an ALWAYS ON data network connection to all their branches and ATM machines. As per the RBI regulations, banks MUST avoid any direct connection between the internet and their core banking system. Hence the best option for banks is to use VPN.</p></li>
<li><h4>ERP and CRM rollouts</h4><p>Many indian companies in manufacturing and services industries are now upgrading their ERP and CRM setups. The highly scalable design of VPN networks allows them to connect their local branches with the company headquarters. It also helps them to have a private network across all the branches than can tranfer data, voice &amp; video.</p></li>
<li><h4>eGovernance</h4><p>As <acronym title="National e-Governance Plan">NEGP</acronym> dictates creation of state data centers, the demand for VPN network will surely increase.</p></li>
</ul>
<p>Tulip is currently the only company that provides countrywide VPN connectivity. The market share of TULIP in VPN services is approx. 30%. TULIP has now upgraded it&#8217;s entire VPN network from copper cables to fibre optic. The company has finished this capital expenditure program and is now ready with fast and reliable fibre optic connection for VPN network.</p>
<p>The company has fibre network of 6000km spread across 300 cities in india. The important thing to note here is:</p>
<p id="tulipkey">The entire effort to upgrade the network to fibre optic connection was done on the receipt of advance customer orders.</p>
<p>TULIP has also acquired license in july 2010 to provide resale of bandwidth services to U.S. based customers that want network connectivity in india. In the wireless network space, TULIP got networking contracts in UP and Gujrat.</p>
